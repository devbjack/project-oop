
public class Equipments {
    private int equipmentId, cost;

    public Equipments(){
    }
    // setter
    public void setEquipmentId(int equipmentId){
        this.equipmentId = equipmentId;
    }
    public void setCost(int cost){
        this.cost = cost;
    }

    // getter
    public int getEquipmentId(){
        return this.equipmentId;
    }
    public int getCost(){
        return  this.cost;
    }

}
